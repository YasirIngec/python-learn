# Python'daki bazı temel veri tipleri:
# int     → tam sayılar
# float   → ondalıklı sayılar
# str     → metinler
# bool    → mantıksal (True/False)

tam_sayi = 10
ondalik = 3.14
metin = "Merhaba"
mantik = True

print(type(tam_sayi))   # <class 'int'>
print(type(ondalik))    # <class 'float'>
print(type(metin))      # <class 'str'>
print(type(mantik))     # <class 'bool'>
