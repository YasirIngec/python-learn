# Sıfırdan 4'e kadar sayıları yazdırır
print("range(5):")
for i in range(5):
    print(i)

# 2'den 10'a kadar olan çift sayıları yazdırır
print("\nrange(2, 11, 2):")
for i in range(2, 11, 2):
    print(i)

# 10'dan geriye doğru 1'e kadar geri sayım
print("\nrange(10, 0, -1):")
for i in range(10, 0, -1):
    print(i)
